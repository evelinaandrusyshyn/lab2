#include <stdio.h>
int main()
{
    int N;
    float M, A, B, C;
    printf("Введіть кількість чобіт, які фабрика виробляє за місяць(N):");
    scanf("%d", &N);
    printf("Введіть відсоток чобіт без підошв у решту місяців(M<=100):");
    scanf("%f", &M);
    if (M > 100) 
    {
        printf("Помилка: M не може бути більше 100!\n");
        return 1;
    }
    A=0.54*N;
    B=11*(M/100)*N;
    C=A+B;
    printf("Загальна кількість чобіт без підошв за рік: %.2f\n", C);
    return 0;
}
